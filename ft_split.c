#include "libft.h"

char	**ft_split(char const *s, char c)
{
	int		i;
	char	**sub;

	i = 1;
	while (*s)
	{
		if (*s == c)
		{
			i++;
		}
		s++;
	}
	*sub = (char **)malloc((i + 1) * sizeof(char *));
}
